חמישה עשר במרץ הוא יום המקביל ליום העצמאות עבור ההונגרים, אם כי הם לא זכו לעצמאות ביום זה.

ב-15 מרץ 1848 פרץ המרד ההונגרי נגד השלטון האוסטרי בהונגריה, ובראשו שושלת בית הבסבורג.

ההונגרים הביסו את האוסטרים מעיר הבירה בודפשט עוד באותו יום, אולם האוסטרים המובסים לא התכוונו לוותר ופנו לעזרת הרוסים, אלו האחרונים פלשו להונגריה וכבשו אותה מחדש מידי המורדים ההונגרים.

המרד כשל אך משהו השתנה מאז, במהלך השנים שלאחר מכן הבינו האוסטרים כי לא יוכלו להמשיך לשלוט בהונגריה, מה שהוביל להקמת האימפריה האוסטרו-הונגרית.

העם ההונגרי מייחס את אותה הבנה למרד של 1848, זוהי אחת הסיבות לכך שהיום הזה הפך לחג לאומי בהונגריה.



