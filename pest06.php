<h2>בית האופרה של בודפשט - Opera House</h2><span class="image fit"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/opera.jpg" alt="בנין האופרה בבודפשט" />בנין האופרה בבודפשט</span>
התכניות להקמת בית האופרה של בודפשט החלו בשנת 1873. 
בתחרות על התכנית האדריכלית הטובה ביותר, זכה האדריכל  מיקלוש יבל (1814 - 1891) (האחראי בין השאר על בניית הבזיליקה), זכייה זו נחשבה להישג המרשים ביותר בקורות חייו.

הבנייה עצמה החלה בשנת 1875 ונמשכה כתשע שנים. טקס הפתיחה התקיים ב -27 בספטמבר 1884 בהשתתפות הקיסר פרנץ יוזף הראשון. 
ההצגה הראשונה בבנין האופרה בוצעה בשיתוף פעולה עם פרנץ ארקל ובנו סאנדור ארקל, על שמם נקרא מאוחר יותר תיאטרון ארקל. 

בית האופרה הבנוי בדמות ארמון ניאו-רנסנסי מארח את האופרה ההונגרית ובית הספר לבלט במשך למעלה מ- 130 שנה ללא שינוי. אלפי התיירים הפוקדים את הבניין מדי שנה זוכים לבד מהמופעים השונים, לחזות באחד המונומנטים החשובים ביותר של המאה ה -19 בבודפשט

בחודש אוגוסט 2016 החל שיפוץ כללי של בניין האופרה, במהלך השיפוץ סגורים לסירוגין חלקים מן הבניין למבקרים.

מועד הסיום המתוכנן לעבודות השיפוץ היה 23 לינואר 2019 אולם העבודות עדיין נמשכות.

