<h2>סמטת גוז'דו - Gozsdu Court</h2>סמטת גוז'דו מהווה את מרכז חיי הלילה של הרובע היהודי בבודפשט, זוהי נקודת מפגש המחברת בין בידור, גסטרונומיה ותרבות. 

אזור הבידור מציע תוכניות ייחודיות בכל ימות השבוע. גוז'דו היא ביתם של אינספור סגנונות מוזיקה ואוכל מיוחדים המספקים חוויה מגוונת לכל הגילאים. 

הסמטה מושכת אליה יותר מ -900,000 מבקרים בשנה, זאת כנראה גם בזכות האופי הקוסמופוליטי שלה, מה שהפך אותה לפופולרית מאוד עבור תושבים ותיירים כאחד.

לגוז'דו יש היסטוריה עשירה שתחילתה בראשית המאה ה -20. הסמטה היתה אז בבעלותו של עורך דין רומני אשר מטרתו העיקרית היתה לחנך ולתמוך בצעירי בבודפשט.

התוכניות לבניית הסמטה החלו בשנת 1900 על ידי קרן גוז'דו (Gozsdu) - הבניה עצמה הושלמה בשנת 1915. באותו זמן התנהלו במקום - בדיוק כמו היום - חיי מסחר ותרבות תוססים.

במהלך מלחמת העולם השנייה היתה הסמטה בתחום גטו פשט הידוע לשמצה והפך אז גם לאתר מורשת עולמית של אונסק"ו.

בשנת 2009 עבר המקום שיפוץ מקיף, החזית שוחזרה במלואה לצורתה המקורית על ידי שתי חברות אחזקה מקומיות.


