<h2>גשר השלשלאות - Széchenyi Chain Bridge</h2><span class="image fit"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/bridge.jpg" alt="מבט על גשר השלשלאות מהצד של פשט כאשר שני האריות בקדמת התמונה" /></span><span class="image right"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/shalshelaot2.jpg" alt="מבט על האריה הימני בגשר השלשלאות מהצד של פשט" /></span>
גשר השלשלאות או בשמו הרשמי – גשר סצ'ני, על שמו של הסופר והפוליטיקאי ההונגרי אישטוון סצ'ני, הינו אחד מסימני ההיכר המרכזיים של בודפשט, ולא לחינם.

זהו גשר הקבע הראשון שחיבר בין שתי הערים (מתוך שלוש) המרכיבות כיום את בודפשט – בודה ופשט.

הגשר עבר טלטלות רבות במהלך השנים, בכל המובנים. בנייתו הסתיימה מיד לאחר נסיון העצמאות ההונגרי של 1848, בטקס הפתיחה שררה אווירת נכאים לאחר שההונגרים היו עסוקים בלקבור את המתים.

מיד לאחר הבניה התברר כי הגשר אינו יציב וכי התנודות שלו מסכנות את עצם קיומו, המצב נמשך עד לשנת 1914 כאשר ברור לכולם כי מדובר בסכנה מיידית.

בשנה זו החלו בשיפוץ המבנה והוא נבנה כולו מחדש בתבנית זהה לגשר המקורי, אולם הפעם תוקנו הטעויות ההנדסיות.

בינואר 1945, עם נסיגת אחרוני הכוחות הגרמנים מאדמת הונגריה, פוצצו כל הגשרים המחברים בין שני חלקי העיר, מטעני נפץ רבי עוצמה הוצבו על כל אחת מן השלשלאות, הפיצוצים הובילו לקריעתן של השלשלאות בצד של פשט בלבד  אך אלו הובילו לקריסת הגשר כולו.

בשנת 1947 הוחלט לשחזר את הגשר. השחזור נעשה על ידי מימון של משקיעים זרים מחוץ להונגריה.

כיום משמש הגשר לתנועת כלי רכב, חשמליות והולכי רגל כמו גם כאתר תיירות.

