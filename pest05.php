<b>היסטוריה:</b><br/>בנין הפרלמנט ההונגרי ממוקם על גדות הדנובה ברובע החמישי של בודפשט. הכיכר והמדשאות סביבו נקראות בשם כיכר קושוט.
בניית המבנה העצום הסתיימה (מאוחר מן המתוכנן) בשנת 1885 והוא מתנשא לגובה של 96 מטרים בדיוק.
בנין הפרלמנט ההונגרי זוכה לתואר המכובד; הפרלמנט השלישי בגודלו בעולם.

