הבנין נבנה בצורה סימטרית לחלוטין כך ששני אגפיו בנויים כתמונת ראי מושלמת.
חלונות הויטראז' של הפרלמנט עוצבו על ידי האמן (היהודי אף הוא) מיקשה רוט ונחשבים למוצג בעל ערך רב בפני עצמו.

