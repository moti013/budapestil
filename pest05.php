<span class="image left"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/bazilika33.jpg" alt="בזיליקת סנט אישטוון בודפשט" /></span>
בזיליקת אישטוון הקדוש או בשמה ההונגרי "בזיליקת סנט סטפן", היא אחת הכנסיות היפות והמוערכות באירופה כולה.
ערכו האדריכלי והאמנותי של בניין הבזיליקה, יחד עם ההיסטוריה ארוכת השנים של הכנסייה, הפכו אותה לשם דבר.

היסטוריה:
תהליך הבנייה של הכנסייה החל בשנת 1846 בתקופתו של מייסד המדינת ההונגרית – המלך סנט סטפן, הבנייה כולה לוותה בתקלות ובקשיים רבים.
יוסף הילד – האדריכל שהחל בתוכנית הבנייה מצא את מותו תוך כדי העבודות, מחליפו הביא ככל הנראה לקריסת כיפת הבזיליקה ולעצירת הבנייה לתקופה של כשנה.
לאחר פינוי ההריסות שונו מעט התוכניות כך שכיפת המבנה תהיה מאוזנת ולא תפעיל משקל יתר על אחת הרוחות.
הבנייה הסתיימה בשנת 1905 וארכה בסופו של דבר יותר מיובל שנים.
במהלך מלחמת העולם השניה ספג בניין הבזיליקה נזקים כבדים, כך שנדרשה בנייתו כמעט מחדש של גג המבנה.

בתוך הבזיליקה נשמרת ידו של המלך סטפן, והיא משמשת גם כמקום קבורתו של הכדורגלן ההונגרי הנערץ – פרנץ פושקש.
בכיפת הבזיליקה הוקמה נקודת תצפית תיירותית אשר ניתן להגיע אליה על ידי טיפוס של 364 מדרגות, או על ידי שימוש באחת המעליות המותקנות במקום.
המעליות הותקנו בתוך פירי הארובות הישנות וצויידו במיטב הטכנולוגיה, מערכת בקרת תדרים אשר הותקנה בהן הופכת את צריכת החשמל שלהן לאפסית.

