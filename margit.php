<h2>פארק המים פאלאטינוס - Palatinus Strand Baths</h2><span class="image fit"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/palatinusbath.jpg" alt="פארק מים בודפשט" /></span>
פארק המים פאלאטינוס - הממוקם בשמורת הטבע של האי מרגיט, הוקם בשנת 1919 כחוף על גדת הדנובה.
בשנת 1921, עם בניית הבריכה הפתוחה הראשונה, שינו החוף והשמורה את שמם למרחצאות.
הפופולריות הגבוהה של הבריכה החדשה שנבנתה, הביאה איתה את הצורך להגדיל את המקום.

<span class="image left"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/palatinus.jpg" alt="פלטינוס" /></span>בשנת 1937 הושק פרויקט אדריכלי ענק שנוהל על ידי האדריכל איסטבאן יאנאקי, כחלק ממנו הוזרמו למקום מי מעיינות תרמיים הנמצאים בקרקעית האי.
במחצית השנייה של שנות השמונים נבנתה הבריכה האולימפית בת חמשת המסלולים, הבריכה הפתוחה ובריכת הגלים.

כיום מושך אליו הפארק מבקרים רבים מכל העולם, מגרשי הספורט ומגרשי המשחקים לילדים, מספקים חוויה מהנה לכל קבוצות הגיל. 
המתחם שופץ בפעם האחרונה בשנת 2002, אז נוספו לפארק המגלשות, הבריכות האתגריות כמו גם התקנים תברואתיים והתקנים לסינון מים מהטובים בעולם.

