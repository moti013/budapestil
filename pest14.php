<h2>טירת ויידהוניאד ומוזיאון החקלאות - Vajdahunyad vára</h2><div class="gallery"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/vajdhoniad5.jpg" alt="טירת ויידהוניאד" /><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/vajdhoniad4.jpg" alt="טירת ויידהוניאד" /><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/vajdhoniad1.jpg" alt="טירת ויידהוניאד" /><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/vajdhoniadff.jpg" alt="טירת ויידהוניאד" /></div>

בשנת 1896 לקראת חגיגות שנת האלף להקמת הונגריה, בחר האדריכל איגנאץ אלפר לבנות בפארק העירוני דגם מוקטן של 21 מבנים מפורסמים, בין המבנים שבחר הייתה טירת ויידהוניאדי (טירת קורבינוב) בטימישוארה שברומניה.
<span class="image left"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/vajdhoniad8.jpg" alt="פסל איגנאץ אלפר" />איגנאץ אלפר</span>
הדגם המקורי עשוי היה עיסת נייר ומעט עצים וייצג שילוב של סגנונות אדריכלות רבים.

בסיום החגיגות פורק הדגם יחד עם הביתן הארעי בו הוא הוצג, אך ההונגרים שהתאהבו כל כך ביצירה החליטו להפוך את הדגם לבניין אבן, זוהי הטירה שאנחנו מכירים כיום.

בניית הטירה החלה עוד באותה שנה והסתיימה כתריסר שנים מאוחר יותר בשנת 1908.

טירת ויידהוניאד משמשת כיום כמקום מושבו הקבוע של מוזיאון החקלאות ההונגרי, המוזיאון עוסק גם או בעיקר בתרבות הציד ומשחקי הציד ההונגרים ומציג נשקים ואביזרים שונים אשר שימשו את הצדים, לא מומלץ לטבעונים.
מול הטירה תוכלו לראות את האנדרטה לזכרו של האדריכל איגנאץ אלפר הלבוש גלימת אדריכל מימי הביניים.

בחצר הטירה מוצג גם הפסל אנונימוס אשר זהותו המלאה איננה ידועה, משערים כי אנונימוס שימש כנוטריון של המלך בלה השלישי.
