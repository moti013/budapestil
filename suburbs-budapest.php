רכבת הילדים נבנתה בשנת 1948 על ידי המשטר הקומוניסטי ששלט אז בהונגריה, מטרתה הייתה חינוך הילדים והנוער לעבודה.

עבודות התשתית להקמת רכבת הילדים על הרי בודה החלו ב-11 באפריל 1948 והתבצעו על ידי חניכי תנועות הנוער הקומוניסטיות שהיו גאים מאוד לקחת חלק בפרויקט.

בכדי להתקבל לעבודות התשתית ולאחר מכן גם לתפעול הקו נדרשו הנערים להצטיין בכל מקצועות הלימוד האחרים.

המקטע הראשון של רכבת הילדים בבודפשט נפתח ב-31 ביולי 1948, הבניה כולה הסתיימה בשנת 1951.

הרכבת מקשרת בין האתרים בגבעות בודה ומתופעלת גם כיום על ידי הילדים בתפקידים השונים.

