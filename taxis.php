<h2>רשימת תחנות מוניות בבודפשט</h2>
<h4>פוּ טקסי - Fő taxi</h4><div class="image left-tiny"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/fotaxi.jpg" alt="מוניות בודפשט" /></div>תחנת המוניות הגדולה בבודפשט, עם למעלה מ-1500 מוניות ומאז שנת 2010 גם בעלת הזכיון הבלעדי על ההעברות משדה התעופה בבודפשט.

דלפק התחנה ממוקם מיד ביציאה מטרמינל 2B (טרמינל הטיסות הבינלאומיות) בצד שמאל.

