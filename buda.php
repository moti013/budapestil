<h2>ארמון שנדור - Sándor Palace</h2><div class="gallery"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/sandorpalace.jpg" alt="ארמון שנדור" /><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/sandorpalace1.jpg" alt="ארמון שנדור" /></div>
בשלישי באפריל 1941 התאבד פאל טלקי - ראש ממשלת הונגריה על ידי יריית אקדח אחת בראשו. 
המיקום: ארמון שנדור.

פאל היה אדם שנוי מאוד במחלוקת, אשר יש לציין כי מייחסים לו דעות אנטישמיות ושנאת יהודים, עקב כך כאשר הונגריה תכננה להציב אנדרטה על שמו מול ארמון שנדור, הופעל לחץ כבד מצד האירגונים היהודיים שהצליחו למנוע זאת.

פאל היה ראש ממשלת הונגריה בשנים 1939-1941, ככל הנראה התקופה הכי חשוכה בהיסטוריה של אירופה.

דמותו היתה מסתורית לכולם, יש אומרים אפילו לעצמו, הוא היה דוקטור לגיאוגרפיה שהשלים בקושי את לימודיו, אבל תוכניותיו ומאמריו לאחר מכן זכו להערכה רבה.

בין השאר הוא היה הראשון שהציע ליצור פדרציה של מדינות אירופאיות לאורך הדנובה, זאת בכדי למנוע את השתלטות הנאצים על אירופה.
הוא ניסה לעניין את האמריקאים בתוכניתו אולם לא נענה בחיוב.

במהלך המלחמה נקט קו מאוד לא ברור כלפי הגרמנים, מצד אחד היה בצד מדינות המערב ומצד שני שיתף איתם פעולה.

בתקופה שקדמה להתאבדותו הציעו לו הגרמנים הצעה לפיה יתן לחיילים הגרמנים מעבר חופשי דרך הונגריה למדינות הבלקן, בתמורה הוצעו לו חזרה את שטחי הונגריה שנלקחו ממנה.

הוא סירב להצעה וכאשר נודע לו כי כוחות גרמניים החלו לעשות את דרכם דרך הונגריה למרות סירובו, התאבד בארמון.

ארמון שנדור נבנה בשנת 1803 בסגנון נאו קלאסי, ואכלס מאז ארבעה עשר ראשי ממשלה הונגרים.
לאחר התאבדותו של פאל נשאר הארמון ריק ומוזנח עד לשנות ה-90 המאוחרות, אז הוחלט לשחזר אותו.
הפרויקט לשחזור הארמון עלה מליארדי דולרים והושלם בשנת 2002, השחזור כלל את כל הריהוט והציורים שהכיל הארמון על פי תמונות וצילומים ישנים.

בשנת 2003 הועבר חוק בפרלמנט ההונגרי אשר הכריז על ארמון שנדור כמקום משכנם הרשמי של נשיאי הונגריה, כך שכיום מתגורר בו נשיא הונגריה יאנוש אדר.

הארמון פתוח למבקרים ימים בודדים בשנה בסופי שבוע בעונת הקיץ, בתוכו אולמות רבים המשמשים למטרות שונות, האולם הגדול בארמון מכונה סלון פטי והוא משמש לפגישות דיפלומטיות.
חדרים נוספים הם החדר הכחול המשמש לוועידות גדולות והחדר האדום העמוס ביצירות אמנות.

אולם המראות בארמון משמש את טקסי קבלת הפנים הרשמיים של ממשלת הונגריה, מחלונותיו ניתן להשקיף על הדנובה החוצה את העיר.


