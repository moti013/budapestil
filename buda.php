<h2>גבעת גלרט - Gellért Hill</h2><div class="image fit"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/gelerthill2.jpg" alt="גבעת גלרט" /></div><div class="image left"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/gelerthill1.jpg" alt="גבעת גלרט" /></div>
גבעת גלרט (המכונה גם הר גלרט) מתנשאת בשיאה לגובה של כמאתיים שלושים וחמישה מטרים והיא נקראת כך על שמו של סנט גלרט.

גלרט שהיה בישוף קתולי נפל או קפץ על פי האגדה מצידה התלול של הגבעה לכיוון הדנובה ומת.

בראש הגבעה נמצאת הסיטדלה, מבצר שהוקם על ידי האוסטרים לאחר המהפכה ההונגרית בשנת 1849.

המצודה שהוקמה לאחר התבוסה של ההונגרים, נבנתה על ידי עובדי כפייה הונגרים מה שיצר שנאה עזה למבנה ולכיבוש האוסטרי שסימל עבורם.

לאחר כינונה של אוסטרו-הונגריה דרשו ההונגרים להרוס את המצודה אולם האוסטרים התנגדו, רק לאחר עזיבתם הסופית בשנת 1897 מימשו ההונגרים את רצונם והרסו חלקית את קירות המבנה.

מאוחר יותר בתקופת מלחמת העולם השנייה עוד שימשה המצודה בתפקיד צבאי פעם אחת נוספת בניסיון להגן על העיר.

