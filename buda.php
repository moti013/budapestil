טירת בודה היא אחד מסמליה הבולטים של העיר בודפשט, בשנת 1987 אף הוכרזה כאתר מורשת עולמי על ידי ארגון אונסקו.
הטירה נבנתה על ידי המלך בלה הרביעי ובנייתה הסתיימה בשנת 1420.

