<h2>רכבת הילדים ורכבל הכסאות - gyermekvasút</h2><span class="image fit"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/gyermek.jpg" alt="רכבת הילדים" />רכבת הילדים</span>
רכבת הילדים נבנתה בשנת 1948 על ידי המשטר הקומוניסטי ששלט אז בהונגריה, מטרתה הייתה חינוך הילדים והנוער לעבודה.

עבודות התשתית להקמת רכבת הילדים על הרי בודה החלו ב-11 באפריל 1948 והתבצעו על ידי חניכי תנועות הנוער הקומוניסטיות שהיו גאים מאוד לקחת חלק בפרויקט.

בכדי להתקבל לעבודות התשתית ולאחר מכן גם לתפעול הקו נדרשו הנערים להצטיין בכל מקצועות הלימוד האחרים.

המקטע הראשון של רכבת הילדים בבודפשט נפתח ב-31 ביולי 1948, הבניה כולה הסתיימה בשנת 1951.

הרכבת מקשרת בין האתרים בגבעות בודה ומתופעלת גם כיום על ידי הילדים בתפקידים השונים.

במקום פועל גם רכבל הכסאות, להגעה לרכבל יורדים בתחנת Janos Hegy ועולים ברגל עד לתחנת המוצא של הרכבל.

ההגעה לרכבת הילדים ממרכז העיר בתחבורה ציבורית הינה באמצעות קו המטרו M2 מתחנת דאק פרנץ (Deák Ferenc tér) עד לתחנת כיכר קלמן (Széll Kálmán tér), יציאה מהמטרו לכיוון כיכר קלמן (לשים לב לשילוט), פונים שמאלה מיד ביציאה לכיוון תחנות האוטובוס ועולים על קו 56A עד לתחנת הובושוולג (Hűvösvölgy) מתחנת האוטובוס חוזרים ברגל בערך חמישים מטר אחורה ופונים ימינה אל רכבת הילדים (Gyermekvasút).  

אפשרות נוספת להגעה לרכבת הילדים היא באמצעות <b>רכבת גלגלי השיניים</b>.

רכבת גלגלי השיניים הנקראת גם קו מספר 60, בנויה על רעיון משנת 1873 לרכבת המטפסת במעלה ההר.

