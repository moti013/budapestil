<h2>דרכי הגעה לצד של בודה</h2>
ישנן שלוש דרכים עיקריות לחציית הנהר

<ul><li>1. רגלית על גבי אחד הגשרים החוצים את הנהר.

הגשרים במרכז בודפשט ובעצם אלו הרלוונטיים עבורכם הם שלושה, הסדר הוא מצפון לדרום.

<b>א. גשר מרגיט</b> - הגשר הכי צפוני כפי שאמרנו והוא לא הבחירה הראשונה לחציית הדנובה מכיוון שתצטרכו להצפין בצד של פשט בכדי להגיע אליו ולהדרים חזרה בצד של בודה בכדי להגיע לרובע אחד.

סיבה אחת כן לחצות דרך גשר מרגיט היא שבאמצעו של הגשר ישנה גישה ל<a href="margit">אי מרגיט</a>, כך שתוכלו אם תרצו לשלב את הטיול באי ביום הטיול בבודה.

<b>ב. גשר סצ'ני</b> - המוכר יותר בשמו המפורסם "גשר השלשלאות".

קצהו האחד של הגשר - בצד של פשט ממוקם במקביל לבזיליקה כך שאם תלכו ממנה לכיוון נהר הדנובה בקו ישר תגיעו בדיוק אליו. לאחר שתחצו אותו תגיעו בצד של בודה לכיכר קלארק אדם (Clark Ádám tér) אשר משמאלה נמצא הרכבל הסגור העולה לגבעת הטירה. בנוסף לרכבל תוכלו לרכוש כרטיס לשאטלים העולים לגבעת הטירה ועוצרים בכמה תחנות עניין בגבעה.

<b>ג. גשר ארז'בט</b> - הדרומי ביותר. הגשר חוצה מנקודה המקבילה לאמצע רחוב ואצי (ואצי אוצה) בצד של פשט ועד לגבעת גלרט ומרחצאות רודש בצד של בודה.

הבחירה באיזה גשר לחצות תלויה במסלול אותו אתם בוחרים לעשות.</li>

<li>2. תחבורה ציבורית, ישנם קווים רבים שניתן להשתמש בהם, רצוי להשתמש בניווט באמצעות מפות גוגל, אם אינכם מחוברים לרשת, אלו הם שני הקווים העיקריים.

<b>א. מטרו אדום M2</b> - קו המטרו האדום מפלס את דרכו במנהרה שנחפרה מתחת לנהר הדנובה, התחנה הראשונה שלו בצד של בודה הינה תחנת Batthyány tér, ממנה תוכלו להגיע בהליכה רגלית קלה של כעשר דקות אל כיכר קלארק אדם (Clark Ádám tér) ומשם אל הטירה.

בצד של פשט תוכלו לעלות על <a href="metro">המטרו</a> בתחנת Deák Ferenc tér ליד הגלגל הענק, או בכל אחת מתחנות ההמשך; Kossuth Lajos Tér ליד בניין הפרלמנט או Arany János utca הסמוכה לבזיליקה.
    
<b>ב. אוטובוס קו 16</b> - מתחנת Deák Ferenc tér ועד לכיכר קלארק אדם.</li>

<li><span class="image left"><img class="lazy" src="preimg.png" data-src="preimg.png" data-srcset="images/boatbkkbuda.jpg" alt="מסלול השיט לבודה" /></span>3. שיט במעבורת של חברת התחבורה הציבורית.
זוהי דרך לא שגרתית אך בהחלט ניתנת לביצוע וזולה או אפילו חינמית אם רכשתם כרטיס יומי, תלת יומי או שבועי לתחבורה הציבורית בבודפשט.
מסלול המעבורות הוא סוג של "זיגזג" בין הצד של בודה לפשט, זאת במטרה לאפשר חצייה נוספת של נהר הדנובה, תוכלו לעלות על המעבורת בתחנות במרכז פשט ולרדת בתחנה בצד של בודה.
מומלץ לבדוק היטב את המסלול ושעות המעבורת בדף <a href="cruises">שיט על הדנובה</a> באתר.</li></ul>

בנוסף לרובע אחד ישנה עוד אטרקציה פופולרית אחת בצד של בודה והיא רכבת הילדים ורכבל הכסאות הסמוך לה. אלה מרוחקים משפת הדנובה ונמצאים לעומקה של בודה באיזור הנקרא דונקסי, להגעה לרכבת הילדים ולרכבל מומלץ לגלול מטה ולקרוא את ההסברים.

מסלולי כל האיזורים בבודפשט מופיעים בדף מסלולי הטיול על גבי גוגל מפות.
